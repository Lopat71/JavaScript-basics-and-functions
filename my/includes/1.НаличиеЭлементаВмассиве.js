// 7.includes() в JavaScript используется для проверки, содержит ли массив (или строка) определённое значение. Этот метод возвращает true, если элемент найден, и false, если нет.

// Синтаксис:
// array.includes(valueToFind, fromIndex);

// Параметры:
// 1. valueToFind (обязательный) — значение, которое нужно найти в массиве или строке.
//     * Для строк используется точное сравнение символов с учётом регистра.
//     * Для массивов используется строгая проверка (так же, как при использовании оператора ===).
// 2. fromIndex (необязательный) — позиция в массиве, с которой следует начать поиск.
//     * Если указано отрицательное значение, поиск начинается с конца массива/строки, но индекс преобразуется в length + fromIndex (то есть -1 означает начать с последнего элемента).
//     * По умолчанию это 0, т.е. поиск начинается с начала.
// Возвращаемое значение:
// * Возвращает true, если valueToFind найден в массиве или строке.
// * Возвращает false, если valueToFind не найден.

// 1. Проверка наличия элемента в массиве чисел
// Дан массив чисел. Используя метод includes(), проверьте, есть ли число 10 в массиве.

// const numbers = [1, 5, 10, 15, 20];
// Ожидаемый результат: true

const numbers = [1, 5, 10, 15, 20];

let hasTen = numbers.includes(10);

console.log(hasTen);
